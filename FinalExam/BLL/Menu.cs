        /// <summary>
        /// 获得数据列表
        /// </summary>
        public List<shaoqi.Model.Menu> DataTableToList(DataTable dt)
        {
            List<shaoqi.Model.Menu> modelList = new List<shaoqi.Model.Menu>();
            int rowsCount = dt.Rows.Count;
            if (rowsCount > 0)
            {
                shaoqi.Model.Menu model;
                for (int n = 0; n < rowsCount; n++)
                {
                    model = dal.DataRowToModel(dt.Rows[n]);
                    if (model != null)
                    {
                        modelList.Add(model);
                    }
                }
            }
            return modelList;
        }

