		public List<shaoqi.Model.Announce> DataTableToList(DataTable dt)
		{
			List<shaoqi.Model.Announce> modelList = new List<shaoqi.Model.Announce>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				shaoqi.Model.Announce model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = dal.DataRowToModel(dt.Rows[n]);
					if (model != null)
                    {
                       
						modelList.Add(model);
					}
				}
			}
			return modelList;
		}

